%wnp------------------------------------------------------
%
%由峰值时间计算欠阻尼系统的近似自然频率(wnp)
%              π
%    wnp = ----------, β = sqrt(1-ζ^2)
%             β*tp
%
%输入:  tp, ζ  (峰值时间, s), (阻尼系数)
%输出:  wnp    (频率, rad/s)
%---------------------------------------------------------

function ret = get_wnp(tp,zeta)
    beta = sqrt(1-zeta^2);
    ret = pi/(beta*tp);
end