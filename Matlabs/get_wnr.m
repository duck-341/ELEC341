%wnr------------------------------------------------------
%
%由上升时间计算欠阻尼系统的近似自然频率(wnr)
%            π-atan(β/ζ)
%    wnr = ---------------, β = sqrt(1-ζ^2)
%               β*tr
%
%输入:  tr, ζ  (上升时间, s), (阻尼系数)
%输出:  wnr    (频率, rad/s)
%---------------------------------------------------------

function ret = get_wnr(tr,zeta)
    beta = sqrt(1-zeta^2);
    ret = (pi-atan(beta/zeta))/(beta*tr);
end