%wns------------------------------------------------------
%
%由稳定时间计算欠阻尼系统的近似自然频率(wns)
%                4
%    wns = ---------------
%               ζ*ts
%
%输入:  ts, ζ  (稳定时间, s), (阻尼系数)
%输出:  wns    (频率, rad/s)
%---------------------------------------------------------

function ret = get_wns(tr,zeta)
    ret = log(50/sqrt(1-zeta^2))/(zeta*tr);
end